package main

import "fmt"

func main() {
    fmt.Println("====>xp\ato<====", `“alert” or bell`)
    fmt.Println("====>xp\bto<====", `backspace`)
    fmt.Println("====>xp\fto<====", `form feed`)
    fmt.Println("====>xp\nto<====", `newline`)
    fmt.Println("====>xp\rto<====", `carriage return`)
    fmt.Println("====>xp\tto<====", `tab`)
    fmt.Println("====>xp\vto<====", `vertical tab`)
    fmt.Println("====>xp\"to<====", `double quote (only within "..." literals)`)
    fmt.Println("====>xp\\to<====", `backslash`)
}
